const STORAGE_KEY = "er-layout-editor-lang";

let locale =
  localStorage.getItem(STORAGE_KEY) ||
  (navigator.language.toLowerCase().startsWith("fr") ? "fr" : "en");

if (!I18N[locale]) locale = "en";

function t(key, params = {}) {
  let s = I18N[locale][key] ?? I18N.en[key] ?? key;
  for (const [k, v] of Object.entries(params)) {
    s = s.split(`{${k}}`).join(String(v));
  }
  return s;
}

function tileCountLabel(n) {
  const key = n === 1 ? "tileCount_one" : "tileCount_other";
  return t(key, { n });
}

function setLocale(lang) {
  if (!I18N[lang] || lang === locale) return;
  locale = lang;
  localStorage.setItem(STORAGE_KEY, lang);
  document.documentElement.lang = lang;
  applyStaticI18n();
  if (typeof window.onLocaleChange === "function") window.onLocaleChange();
}

function applyStaticI18n() {
  document.title = t("docTitle");
  document.querySelectorAll("[data-i18n]").forEach((el) => {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
    el.setAttribute("aria-label", t(el.dataset.i18nAria));
  });
  document.querySelectorAll("[data-i18n-title]").forEach((el) => {
    el.title = t(el.dataset.i18nTitle);
  });
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.classList.toggle("active", btn.dataset.lang === locale);
    btn.setAttribute("aria-pressed", btn.dataset.lang === locale ? "true" : "false");
  });
}

function initI18n() {
  document.documentElement.lang = locale;
  applyStaticI18n();
  document.querySelectorAll(".lang-btn").forEach((btn) => {
    btn.addEventListener("click", () => setLocale(btn.dataset.lang));
  });
}

initI18n();
